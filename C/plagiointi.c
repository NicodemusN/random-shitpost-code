  int main()
{
  int fahr, celsius;
  int lower, upper, step;

  lower = 0;    /* alinmahdollinen  */
  upper = 300;  /* ylinmahdollinen  */
  step =20;     /* askel koko  */

  fahr = lower;
  while (fahr <= upper) {
      celsius = 5 * (fahr-32) / 9;
      printf("%d\t%d\n", fahr, celsius);
      fahr = fahr + step;
  }
  return 0;
}
